#!/usr/bin/python3

print('Range(5)=', end=" ")
for i in range(5):
    print(i, end=" ")

print('\nRange(1, 5)=', end=" ")
for i in range(1, 5):
    print(i, end=" ")

print('\nRange(3, 8)=', end=" ")
for i in range(3, 8):
    print(i, end=" ")

print('\nRange(1, 11, 2)=', end=" ")
for i in range(1, 11, 2):
    print(i, end=" ")

print('\nRange(0, 11, 2)=', end=" ")
for i in range(0, 11, 2):
    print(i, end=" ")